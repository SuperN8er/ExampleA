
## Primitive Data Types

"""
The most basic form of an object in Python that is builtin.

"""

string = "This is a string"  # TYPE: str
integer = 1024               # TYPE: int
decimal = 3.14               # TYPE: float

print(type(string))
print(type(integer))
print(type(decimal))
# <class 'str'>
# <class 'int'>
# <class 'float'>

print(type(str(123.426537)))
print(type(int(3.14)))
print(type(float(3)))
print(type(int("132312")))
print(type(float("132312.32576")))
# <class 'str'>
# <class 'int'>
# <class 'float'>
# <class 'int'>
# <class 'float'>

